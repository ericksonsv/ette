    {
        $drivers = Driver::whereNotIn('id',[1])->where('active', true)->pluck('name', 'id');

        if (!count($drivers)) {
            return redirect()->route('drivers.index')->with('warning', __('You need to active or create a driver before'));
        }

        return view('orders.create');
    }

